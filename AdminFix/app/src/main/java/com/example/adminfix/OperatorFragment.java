    // TODO: Rename method, update argument and hook method into UI event
    public void onButtonPressed(Uri uri) {
        if (mListener != null) {
            mListener.onFragmentInteraction(uri);
        }
    }


    @Override
    public void onViewCreated(@NonNull View view, @Nullable Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);
        bottomNavOperator = getView().findViewById(R.id.bottomNavOperator);
        changeOperatorFragment(new AddOperatorFragment(), "");
        bottomNavOperator.setOnNavigationItemSelectedListener(
                new BottomNavigationView.OnNavigationItemSelectedListener() {
                    @Override
                    public boolean onNavigationItemSelected(@NonNull MenuItem menuItem) {
                        if (menuItem.getItemId() == R.id.menuaddpeople){
                            changeOperatorFragment(new AddOperatorFragment(), "");
                        }
                        else if (menuItem.getItemId() == R.id.menueditpeople){
                            changeOperatorFragment(new EditOperatorFragment(), "");
                        }
                        return true;
                    }
                }
        );
    }

    public void changeOperatorFragment(Fragment f, String data) {
        FragmentManager fz = getFragmentManager();
        FragmentTransaction ft = fz.beginTransaction();
        Bundle bundle = new Bundle();
        bundle.putString("data", data);
        f.setArguments(bundle);
        ft.replace(R.id.containerOperator, f);
        ft.commit();
    }


